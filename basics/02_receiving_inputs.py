# In Python, we have another input function called input.
# - Using this function, we can take inputs from user
name = input("What is your name? ")
# Note the space between the ? and Double Quotes.
# - On Running the program, you will see the space after the ?
# Input() will return the value entered by the user.
# - In the above code, we are now declaring a variable "name" and setting value returned by the Input function
print("Hello " + name)
# Note : In the above Print, we are doing String Concatenation.
# We are combining Static string "Hello " with String variable Name
