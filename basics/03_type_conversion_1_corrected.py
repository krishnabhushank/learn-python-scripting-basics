# In Python, we have come across 3 datatypes : Numbers, String and Boolean
# There are times, we need to convert value of a Variable from one type to another
birth_year = input("What is your birth year? ")
# In Python, we have a bunch of Built-in function to convert type of variables
age = 2023 - int(birth_year)  # int() will provide a Numerical representation of the Input, say 1982.
print(age)

# We have other built-in functions for type conversion
# - float(): Convert value to a Decimal
# - bool(): Convert value to a Boolean
# - str(): Convert value to a String
