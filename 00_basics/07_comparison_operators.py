# Operator to compare values.
x = 3 > 2
# 3 > 2 is a Boolean expression. It produces a Boolean values as results
print(x)

# We have other operators, like below
x = 3 >= 2
print(x)
x = 3 < 2
print(x)
x = 3 <= 2
print(x)

# Equality operator
x = 3 == 2  # Do not confuse == with =. = is assignment operator
print(x)

# Non-Equality operator
x = 3 != 2
print(x)

